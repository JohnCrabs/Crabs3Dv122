/*
 * Draw greed to screen.
 * The grid starts from (Ymin, Zmin) and finish to (Ymax, Zmax).
 * The distance between lines is equal to step.
 */
void gridYZ(float Ymin, float Zmin, float Ymax, float Zmax, float step);
